package org.leagueoftests.app;

public class Champions {
    public int id;
    public String championName;
    public int championPriceBE;
    public int championPriceRP;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getChampionName() {
        return championName;
    }

    public void setChampionName(String championName) {
        this.championName = championName;
    }

    public int getChampionPriceBE() {
        return championPriceBE;
    }

    public void setChampionPriceBE(int championPriceBE) {
        this.championPriceBE = championPriceBE;
    }

    public int getChampionPriceRP() {
        return championPriceRP;
    }

    public void setChampionPriceRP(int championPriceRP) {
        this.championPriceRP = championPriceRP;
    }

}
